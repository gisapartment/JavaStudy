public class AssignmentAndOperator {
	public static void main(String[] args) {
		int x = 2;
		x += 1;
		System.out.println(x);
		x -= 1;
		System.out.println(x);
		x *= 2;
		System.out.println(x);
		x /= 2;
		System.out.println(x);
		x %= 10;
		System.out.println(x);
	}
}
